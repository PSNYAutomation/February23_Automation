package variables;

public class Calculator {

	//this metod will add two int values 
	public int add(int a, int b) {

		int result = a + b;
		return result;
	}
	public int sub(int a, int b) {
		
		int result = a - b;
		return result;
	}
	public int mul(int a, int b) {
		
		int result = a * b;
		return result;
	}
	public int div(int a, int b) {
		
		int result = a / b;
		return result;
	}

}
